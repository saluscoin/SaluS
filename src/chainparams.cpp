// Convert the pnSeeds6 array into usable address objects.
static void convertSeed6(std::vector<CAddress> &vSeedsOut, const SeedSpec6 *data, unsigned int count)
{
    // It'll only connect to one or two seed nodes because once it connects,
    // it'll get a pile of addresses with newer timestamps.
    // Seed nodes are given a random 'last seen time' of between one and two
    // weeks ago.
    const int64_t nOneWeek = 7*24*60*60;
    for (unsigned int i = 0; i < count; i++)
    {
        struct in6_addr ip;
        memcpy(&ip, data[i].addr, sizeof(ip));
        CAddress addr(CService(ip, data[i].port));
        addr.nTime = GetTime() - GetRand(nOneWeek) - nOneWeek;
        vSeedsOut.push_back(addr);
    }
}

class CMainParams : public CChainParams {
public:
    CMainParams() {
        // The message start string is designed to be unlikely to occur in normal data.
        // The characters are rarely used upper ASCII, not valid as UTF-8, and produce
        // a large 4-byte int at any alignment.

        pchMessageStart[0] = 0xd3;
        pchMessageStart[1] = 0xc5;
        pchMessageStart[2] = 0xa7;
        pchMessageStart[3] = 0x21;
        vAlertPubKey = ParseHex("048191b8b5cefcb54f5eb803aa15bd10d4ad003f30be7b83c091d53e88f02715c5ca14374bb33205145a555e7594836ae5cc40475f25f59afff720032455bcbbb3");
        nDefaultPort = 22534;
        nRPCPort = 22530;
        bnProofOfWorkLimit = CBigNum(~uint256(0) >> 20);

        const char* pszTimestamp = "Louis XIV declares war on the Netherlands.";
        std::vector<CTxIn> vin;
        std::vector<CTxOut> vout;
        CTransaction txNew(1, 1448600434, vin, vout, 0);
        vin.resize(1);
        vin[0].scriptSig = CScript() << 0 << CBigNum(42) << vector<unsigned char>((const unsigned char*)pszTimestamp, (const unsigned char*)pszTimestamp + strlen(pszTimestamp));
        vout.resize(1);
        vout[0].SetEmpty();
        genesis.vtx.push_back(txNew);
        genesis.hashPrevBlock = 0;
        genesis.hashMerkleRoot = genesis.BuildMerkleTree();
        genesis.nVersion = 1;
        genesis.nTime    = 1448600434;
        genesis.nBits    = bnProofOfWorkLimit.GetCompact();
        genesis.nNonce   = 319779;

        hashGenesisBlock = genesis.GetHash();
        assert(hashGenesisBlock == uint256("0x000003f07d368792f736f3098ad7201bb821883fbb66b27a8ee65be273847e02"));
        assert(genesis.hashMerkleRoot == uint256("0x169309cb25cd5c63a0cd5e02540d2ac613153a33694c6ee665ad0b598408bd1b"));

        vSeeds.push_back(CDNSSeedData("198.50.243.69", "198.50.243.69"));
        vSeeds.push_back(CDNSSeedData("208.110.70.10", "208.110.70.10"));
        vSeeds.push_back(CDNSSeedData("104.167.2.136", "104.167.2.136"));
        vSeeds.push_back(CDNSSeedData("162.0.234.3", "162.0.234.3"));
        vSeeds.push_back(CDNSSeedData("sls.node.vg", "sls.node.vg"));
        vSeeds.push_back(CDNSSeedData("salus.node.vg", "salus.node.vg"));

        base58Prefixes[PUBKEY_ADDRESS] = std::vector<unsigned char>(1,63);
        base58Prefixes[SCRIPT_ADDRESS] = std::vector<unsigned char>(1,23);
        base58Prefixes[SECRET_KEY] =     std::vector<unsigned char>(1,191);
        base58Prefixes[EXT_PUBLIC_KEY] = boost::assign::list_of(0x04)(0x88)(0xB2)(0x1E).convert_to_container<std::vector<unsigned char> >();
        base58Prefixes[EXT_SECRET_KEY] = boost::assign::list_of(0x04)(0x88)(0xAD)(0xE4).convert_to_container<std::vector<unsigned char> >();

        nBip44Id = 0x8000023c;

        convertSeed6(vFixedSeeds, pnSeed6_main, ARRAYLEN(pnSeed6_main));

        nLastPOWBlock = 75;
    }

    virtual const CBlock& GenesisBlock() const { return genesis; }
    virtual Network NetworkID() const { return CChainParams::MAIN; }

    virtual const vector<CAddress>& FixedSeeds() const {
        return vFixedSeeds;
    }
protected:
    CBlock genesis;
    vector<CAddress> vFixedSeeds;
};
static CMainParams mainParams;


